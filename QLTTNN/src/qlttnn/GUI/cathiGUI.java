    private void btnThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemMouseClicked
       cathiBLL bll = new cathiBLL();
        txtMaca.setText(bll.remindCathiID());
        qlttnn.DTO.cathiDTO gt = new qlttnn.DTO.cathiDTO();
        gt.setCathiID(Integer.parseInt(txtMaca.getText()));
       
        gt.setGiothi(txtGiothi.getText());

        Vector head = new Vector();
        head.add(gt.getCathiID());
     
        head.add(gt.getGiothi());
        ;

        int rs = bll.them(gt);
        if(rs == 1){
            table.addRow(head);
            jt.setModel(table);
            JOptionPane.showMessageDialog(null, "Thêm thành công");
        }else{
            JOptionPane.showMessageDialog(null, "Mã đã tồn tại. Thêm thất bại");
        }
        cleanView();
    }//GEN-LAST:event_btnThemMouseClicked

