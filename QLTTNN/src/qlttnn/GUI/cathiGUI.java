    public void doDuLieuLenBang() {
        cathiBLL bus = new cathiBLL();
        try {
            bus.docDuLieu();
            table = (DefaultTableModel) jt.getModel();
            table.setRowCount(0);
            for (qlttnn.DTO.cathiDTO kh : bus.ds) {
                Vector vt = new Vector();
                vt.add(kh.getCathiID());
               
                vt.add(kh.getGiothi());
             
                
               
                table.addRow(vt);
            }
            jt.setModel(table);
        } catch (Exception ex) {
            Logger.getLogger(cathiDTO.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

