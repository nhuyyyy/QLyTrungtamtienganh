    private void btnThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemMouseClicked
        khoathiBLL bus = new khoathiBLL();
        txtMakhoathi.setText(bus.remindKhoathiID());
        qlttnn.DTO.khoathiDTO gt = new qlttnn.DTO.khoathiDTO();
        gt.setKhoathiID(Integer.parseInt(txtMakhoathi.getText()));
        System.out.println(dateStart.getDate());
        gt.setNgaythi(dateStart.getDate());
        gt.setTenkhoathi(txtTenkhoa.getText());

        Vector head = new Vector();
        head.add(gt.getKhoathiID());
        DateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        head.add(format.format(gt.getNgaythi()));
        head.add(gt.getTenkhoathi());
        ;

        int rs = bus.them(gt);
        if(rs == 1){
            table.addRow(head);
            jt.setModel(table);
            JOptionPane.showMessageDialog(null, "Thêm thành công");
        }else{
            JOptionPane.showMessageDialog(null, "Mã đã tồn tại. Thêm thất bại");
        }
        cleanView();
    }//GEN-LAST:event_btnThemMouseClicked

