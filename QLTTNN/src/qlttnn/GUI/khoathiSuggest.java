    private void btnSuaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSuaMouseClicked
        // TODO add your handling code here:
        khoathiBLL bll = new khoathiBLL();
        int i = jt.getSelectedRow();
        if (i >= 0) {

            int result = JOptionPane.showConfirmDialog(null, "Xác nhận sửa ?", "Xác nhận", JOptionPane.YES_NO_OPTION);
            if (result == JOptionPane.YES_OPTION) {
                qlttnn.DTO.khoathiDTO khoathi = new khoathiDTO();
                khoathi.setKhoathiID(Integer.parseInt(txtMakhoathi.getText()));
                khoathi.setTenkhoathi(txtTenkhoa.getText());
                khoathi.setNgaythi(dateStart.getDate());

                DateFormat format = new SimpleDateFormat("yyyy-MM-dd");
                int check = bll.sua(i, khoathi);
                if (check == 1) {
                    table.setValueAt(khoathi.getKhoathiID(), i, 1);
                    table.setValueAt(khoathi.getTenkhoathi(), i, 3);

                    table.setValueAt(format.format(khoathi.getNgaythi()), i, 2);

                    jt.setModel(table);
                    JOptionPane.showMessageDialog(null, "Sửa thành công");
                }
                cleanView();
            }
        }
    }//GEN-LAST:event_btnSuaMouseClicked

